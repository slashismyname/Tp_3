    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
                
        
        mC.setNom(txt_nom.getText());
        mC.setPostnom(txt_postnom.getText());
        mC.setMaila(txt_maila.getText());
        mC.setPassword(txt_password.getText());
        
        double sold = Double.parseDouble(txt_solde.getText());
        
        mC.setSolde(sold);
        
        mCU.Enregistrer(mC);
        
        try {
            //map pour recuperer le valeur de la base et les afficher

            mCU.map_(txt_maila.getText(), "client");
            nom = mCU.nom_mcu;
            postnom = mCU.postnom_mcu;
            id = mCU.id_mcu;
            solde = mCU.solde_mcu;
        } catch (SQLException ex) {
            Logger.getLogger(enregistrer_.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        // Cr√©ez une nouvelle instance de la page 2.
        afficher_ page3 = new afficher_();

        // Rendez la page 2 visible.
        page3.setVisible(true);
                
        this.dispose();
        
    }//GEN-LAST:event_jToggleButton1ActionPerformed

